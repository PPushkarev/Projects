per_cent = {'TKB': 5.6, 'СКБ': 5.9, 'ВТБ': 4.28, 'СБЕР': 4.0}
money= input("Введите сумму предполагаемого депозита:")
money= int(money)

deposit= [per_cent['TKB']*money,per_cent['СКБ']*money,per_cent['ВТБ']*money,per_cent['СБЕР']*money]

print("TKB:",int(deposit[0]),"СКБ",int(deposit[1]),"ВТБ",int(deposit[2]),"СБЕР",int(deposit[3]))
max_deposit = max(deposit)
print("Максимальная сумма, которую вы можете заработать", int(max_deposit))